package common.net;

/**
 * Типы команд, поддерживаемых сервером.
 * 
 * <p>
 * Каждое значение перечисления соответствует одной команде.
 * Используется в {@link common.net.Request} для указания того,
 * какую операцию должен выполнить сервер.
 * </p>
 */
public enum CommandType {
    /** Вывести информацию о коллекции */
    INFO,
    /** Вывести все элементы коллекции */
    SHOW,
    /** Добавить новый элемент */
    ADD,
    /** Обновить элемент по id */
    UPDATE,
    /** Удалить элемент по id */
    REMOVE_BY_ID,
    /** Очистить коллекцию */
    CLEAR,
    /** Вставить элемент по индексу */
    INSERT_AT,
    /** Добавить элемент, если он больше максимального */
    ADD_IF_MAX,
    /** Удалить элементы, превышающие заданный */
    REMOVE_GREATER,
    /** Вывести элемент с максимальной зарплатой */
    MAX_BY_SALARY,
    /** Подсчитать элементы с датой начала позже указанной */
    COUNT_GREATER_THAN_START_DATE,
    /** Вывести уникальные значения дат начала */
    PRINT_UNIQUE_START_DATE;
}