package client.commands;

import common.net.Response;

/**
 * Интерфейс клиентской команды.
 * 
 * Каждая команда предоставляет:
 * <ul>
 * <li>Название и описание — для вывода справки (команда {@code help})</li>
 * <li>Метод {@code execute} — принимает массив строк-аргументов,
 * отправляет запрос на сервер и возвращает ответ</li>
 * </ul>
 * 
 * Реализации этого интерфейса не содержат бизнес-логики — они только
 * собирают {@link common.net.Request} и передают его в {@link main.Client}.
 */
public interface ClientCommand {

    /**
     * Возвращает название команды, по которому она вызывается из консоли.
     * 
     * @return строка с именем команды (например, "add", "show")
     */
    String getName();

    /**
     * Возвращает краткое описание того, что делает команда.
     * Используется командой {@code help} для вывода списка доступных команд.
     * 
     * @return строка с описанием команды
     */
    String getDescription();

    /**
     * Выполняет команду: проверяет аргументы, формирует запрос, отправляет на
     * сервер
     * и возвращает ответ.
     * 
     * @param args массив строк-аргументов, полученных из командной строки
     *             (уже без названия самой команды)
     * @return {@link common.net.Response} — ответ от сервера.
     *         Может быть неуспешным ({@code status = false}), если аргументы
     *         неверны
     *         или произошла ошибка соединения
     */
    Response execute(String... args);
}